//1. 기본 프레임 (JFrame)
// 한국어: 1. 기본 프레임 (JFrame) - Swing의 최상위 컨테이너
// 중국어: 1. 基本框架 (JFrame) - Swing的顶级容器

package day1;

import javax.swing.*;

public class BasicFrame01 {
    public static void main(String[] args) {
        // ============================================================
        // 한국어: JFrame 객체 생성 - Swing 애플리케이션의 메인 창
        //         생성자 매개변수: 창 제목을 "Swing 예제"로 설정
        // 중국어: 创建JFrame对象 - Swing应用程序的主窗口
        //         构造函数参数: 设置窗口标题为 "Swing 예제"
        // ============================================================
        JFrame frame = new JFrame("Swing 예제");
        
        // ============================================================
        // 한국어: setSize(너비, 높이) - 프레임의 크기를 픽셀 단위로 설정
        //         여기서는 가로 400px, 세로 300px로 지정
        // 중국어: setSize(宽度, 高度) - 以像素为单位设置框架大小
        //         这里设置为宽度400px, 高度300px
        // ============================================================
        frame.setSize(400, 300);
        
        // ============================================================
        // 한국어: setDefaultCloseOperation() - 창 닫기 버튼 클릭 시 동작 지정
        //         JFrame.EXIT_ON_CLOSE: 창을 닫으면 프로그램도 종료
        //         다른 옵션: DISPOSE_ON_CLOSE(창만 닫음), DO_NOTHING_ON_CLOSE(무시)
        // 중국어: setDefaultCloseOperation() - 指定点击窗口关闭按钮时的行为
        //         JFrame.EXIT_ON_CLOSE: 关闭窗口时程序也终止
        //         其他选项: DISPOSE_ON_CLOSE(仅关闭窗口), DO_NOTHING_ON_CLOSE(忽略)
        // ============================================================
        frame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        
        // ============================================================
        // 한국어: setVisible(true) - 프레임을 화면에 표시
        //         false로 설정하면 프레임이 보이지 않음 (기본값은 false)
        //         모든 컴포넌트 추가가 완료된 후 마지막에 호출하는 것이 좋음
        // 중국어: setVisible(true) - 在屏幕上显示框架
        //         设置为false则框架不可见 (默认值为false)
        //         建议在所有组件添加完成后再调用
        // ============================================================
        frame.setVisible(true);
        
        // ============================================================
        // 한국어: 참고사항 - Swing 프로그램의 특징
        //         1. 모든 Swing 컴포넌트는 javax.swing 패키지에 포함
        //         2. JFrame은 최상위 컨테이너로 다른 컴포넌트들을 포함
        //         3. 기본적으로 프레임은 화면 좌측 상단에 위치
        //         4. setLocationRelativeTo(null)로 화면 중앙에 배치 가능
        // 중국어: 注意事项 - Swing程序的特点
        //         1. 所有Swing组件都包含在javax.swing包中
        //         2. JFrame是顶级容器，可包含其他组件
        //         3. 默认情况下框架位于屏幕左上角
        //         4. 可使用setLocationRelativeTo(null)将窗口置于屏幕中央
        // ============================================================
    }
}

